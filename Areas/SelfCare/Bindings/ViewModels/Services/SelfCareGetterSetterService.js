SelfCareContent.service('SelectedTicketNumberService', function () {
    var selectedTicket;

    var setVal = function (Data) {
        selectedTicket = Data;
    };

    var getVal = function () {
        return selectedTicket;
    };

    return {
        setVal: setVal,
        getVal: getVal
    };
});

